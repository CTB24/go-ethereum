// Node represents a host on the network.
// The fields of Node may not be modified.
type Node struct {
	n enode.Node

	// This is a cached copy of the ID which is used for node distance calculations. This
	// is part of Node in order to make it possible to write tests that need a node at a
	// certain distance. In those tests, the content of 'id' will not actually correspond
	// with 'pubkey'.
	id enode.ID

	// Time when the node was added to the table.
	addedAt time.Time
}

type encPubkey [64]byte

func encodePubkey(key *ecdsa.PublicKey) encPubkey {
	var e encPubkey
	math.ReadBits(key.X, e[:len(e)/2])
	math.ReadBits(key.Y, e[len(e)/2:])
	return e
}

func decodePubkey(e encPubkey) (*ecdsa.PublicKey, error) {
	p := &ecdsa.PublicKey{Curve: crypto.S256(), X: new(big.Int), Y: new(big.Int)}
	half := len(e) / 2
	p.X.SetBytes(e[:half])
	p.Y.SetBytes(e[half:])
	if !p.Curve.IsOnCurve(p.X, p.Y) {
		return nil, errors.New("invalid secp256k1 curve point")
	}
	return p, nil
}

func (e encPubkey) id() enode.ID {
	return enode.ID(crypto.Keccak256Hash(e[:]))
}

func newNode(n *enode.Node) *Node {
	return &Node{n: *n, id: n.ID()}
}

func convertNodes(ns []*enode.Node) []*Node {
	result := make([]*Node, 0, len(ns))
	for _, n := range ns {
		result = append(result, newNode(n))
	}
	return result
}

func (n *Node) addr() *net.UDPAddr {
	return &net.UDPAddr{IP: n.n.IP(), Port: n.n.UDP()}
}

// Incomplete returns true for nodes with no IP address.
func (n *Node) Incomplete() bool {
	return n.n.IP() == nil
}

// The string representation of a Node is a URL.
// Please see ParseNode for a description of the format.
func (n *Node) String() string {
	return n.n.String()
}

// recoverNodeKey computes the public key used to sign the
// given hash from the signature.
func recoverNodeKey(hash, sig []byte) (key encPubkey, err error) {
	pubkey, err := secp256k1.RecoverPubkey(hash, sig)
	if err != nil {
		return key, err
	}
	copy(key[:], pubkey)
	return key, nil
}

// distcmp compares the distances a->target and b->target.
// Returns -1 if a is closer to target, 1 if b is closer to target
// and 0 if they are equal.
func distcmp(target, a, b enode.ID) int {
	for i := range target {
		da := a[i] ^ target[i]
		db := b[i] ^ target[i]
		if da > db {
			return 1
		} else if da < db {
			return -1
		}
	}
	return 0
}

